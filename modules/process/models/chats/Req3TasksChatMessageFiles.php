<?php

namespace app\modules\process\models\chats;

use Yii;
use yii\db\ActiveRecord;

/**
 * This is the model class for table "req3_tasks_chat_message_files".
 *
 * @property integer               $id
 * @property integer               $message_id
 * @property string                $orig_name
 * @property string                $save_name
 * @property string                $path
 *
 * @property Req3TasksChatMessages $message
 */
class Req3TasksChatMessageFiles extends ActiveRecord
{
    // ============================================================================
    // ============================== КОНСТАНТЫ ===================================
    // ============================================================================

    // ============================================================================
    // ============================== ДОПОЛНИТЕЛЬНЫЕ ПОЛЯ =========================
    // ============================================================================

    // ============================================================================
    // ============================== ИНИТ ========================================
    // ============================================================================
    public function init()
    {
    }

    public static function tableName()
    {
        return 'req3_tasks_chat_message_files';
    }


    public function attributeLabels()
    {
        return [
            'id'         => 'ID',
            'message_id' => 'Message ID',
            'orig_name'  => 'Orig Name',
            'save_name'  => 'Save Name',
            'path'       => 'Path',
        ];
    }


    public static function find()
    {
        return new \app\modules\process\models\_query\Req3TasksChatMessageFilesQuery(get_called_class());
    }

    // ============================================================================
    // ============================== ПРАВИЛА =====================================
    // ============================================================================
    public function rules()
    {
        return [
            [['message_id', 'orig_name', 'save_name', 'path'], 'required'],
            [['message_id'], 'integer'],
            [['orig_name', 'save_name', 'path'], 'string', 'max' => 256],
        ];
    }

    // ============================================================================
    // ============================== СЦЕНАРИИ ====================================
    // ============================================================================

    // ============================================================================
    // ============================== ГЕТТЕРЫ =====================================
    // ============================================================================
    public function getMessage()
    {
        return $this->hasOne(Req3TasksChatMessages::class, ['id' => 'message_id']);
    }

    // ============================================================================
    // ============================== СЕТТЕРЫ =====================================
    // ============================================================================

    // ============================================================================
    // ============================== СОБЫТИЯ СОХРАНЕНИЯ ==========================
    // ============================================================================

    // ============================================================================
    // ============================== СОБЫТИЯ УДАЛЕНИЯ ============================
    // ============================================================================

    // ============================================================================
    // ============================== ЧТО КАСАЕТСЯ OBJECT =========================
    // ============================================================================
    public function getFileSize()
    {
        $directory = Yii::getAlias("@app/modules/process/files/chats/");
        if (!file_exists($directory . $this->path . $this->save_name)) {
            return 0;
        }
        return filesize($directory . $this->path . $this->save_name);
    }

    // ============================================================================
    // ============================== STATIC ======================================
    // ============================================================================
}